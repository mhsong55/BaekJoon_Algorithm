package Problem;

import java.util.Scanner;

public class Prob10869 {
    public static void main(String[] args) {
        //사칙연산
        Scanner sc = new Scanner(System.in);
        int A = sc.nextInt();
        int B = sc.nextInt();
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A % B);
    }
}
